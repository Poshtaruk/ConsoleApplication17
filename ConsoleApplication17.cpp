// Завдання: Знайти добуток елементів головної діагоналі квадратної матриці

#include <iostream>
#include <vector>
#include <windows.h>

using namespace std;

int main()
{
    SetConsoleOutputCP(1251); // Встановлюємо кодування для відображення кирилиці в консолі
    int size = 3; // Розмір квадратної матриці
    vector<vector<int>> matrix(size, vector<int>(size)); // Створення квадратної матриці

    // Заповнення матриці випадковими числами
    for (int i = 0; i < size; i++) {
        for (int j = 0; j < size; j++) {
            matrix[i][j] = rand() % 21 - 10; // Числа від -10 до 10
        }
    }

    // Виведення матриці
    cout << "Матриця: \n";
    for (int i = 0; i < size; i++) {
        for (int j = 0; j < size; j++) {
            cout << matrix[i][j] << "\t"; // Виведення кожного елемента матриці
        }
        cout << endl; // Перехід на новий рядок після кожного рядка матриці
    }

    // Обчислення добутку елементів головної діагоналі
    int product = 1; // Змінна для зберігання добутку, ініціалізована як 1
    for (int i = 0; i < size; i++) {
        product *= matrix[i][i]; // Множення елементів головної діагоналі
    }

    // Виведення результату
    cout << "Добуток елементів головної діагоналі: " << product << endl;

    return 0;
}